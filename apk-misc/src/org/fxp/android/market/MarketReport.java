package org.fxp.android.market;

public class MarketReport {
	MarketSubject marketSubject;
	MarketApp marketApp;
	MarketGame marketGame;
	MarketHome marketHome;
	MarketNews marketNews;
	MarketRank marketRank;
	
	public MarketSubject getMarketSubject() {
		return marketSubject;
	}
	public MarketApp getMarketApp() {
		return marketApp;
	}
	public MarketGame getMarketGame() {
		return marketGame;
	}
	public MarketHome getMarketHome() {
		return marketHome;
	}
	public MarketNews getMarketNews() {
		return marketNews;
	}
	public MarketRank getMarketRank() {
		return marketRank;
	}

}
